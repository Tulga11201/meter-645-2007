void Set_Esam_IO_Dir(void)
{
    SET_ESAM_PWR_DIR;      //输出:1-----开启电源
    SET_ESAM_RST_DIR;      //输出:0-------有效
    SET_ESAM_CLK_DIR;      //输出:0-------使能时钟    
}
/**********************************************************************************
**********************************************************************************/ 
void Set_Esam_Pwr(INT8U Level)
{
  SET_ESAM_PWR_DIR;        //输出:1-----开启电源
  SET_ESAM_POWER(Level);
}
/**********************************************************************************
**********************************************************************************/ 
void Set_Esam_Rst(INT8U Level)
{
  SET_ESAM_RST_DIR;
  SET_ESAM_RST(Level);
}
/**********************************************************************************
**********************************************************************************/ 
void Set_Esam_Clk(INT8U Level)
{
  SET_ESAM_CLK_DIR;
  SET_ESAM_CLK(Level);
}


